package Sub6;

public class Book {
    String title;
    String author;
    int price;

    Book(){

    }

    Book(String title, String author){
        this.title = title;
        this.author = author;
    }

    Book(String title, String author, int price){
        this.title = title;
        this.author = author;
        this.price = price;
    }

}
